package Exam01;

public class Ex02 {
    public static void main(String[] args) {
        System.out.println( 1 % 3 ); // 1을 3으로 나눈 나머지 // 1
        System.out.println( 2 % 3 ); // 2를 3으로 나눈 나머지 // 2
        System.out.println( 3 % 3 ); // 3을 3으로 나눈 나머지 // 0
        System.out.println( 4 % 3 ); // 4을 3으로 나눈 나머지 // 1
        System.out.println( 5 % 3 ); // 5을 3으로 나눈 나머지 // 2
        System.out.println( 6 % 3 ); // 6을 3으로 나눈 나머지 // 0
        System.out.println( 7 % 3 ); // 7을 3으로 나눈 나머지 // 1
        // 1,2,0 의 규칙성이 보인다
    }
}
